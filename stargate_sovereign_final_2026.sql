);

-- 4. THE STARGATE CONSTANT TRIGGER (1.42x MULTIPLIER)
CREATE OR REPLACE TRIGGER STARGATE_ADMIN.TRG_QUANTUM_ALPHA
BEFORE INSERT OR UPDATE ON STARGATE_ADMIN.STARGATE_TELEMETRY
FOR EACH ROW
DECLARE
    c_factor CONSTANT NUMBER := 1.42;
BEGIN
    IF :NEW.quantum_ready_status = 'Advantage' THEN
        :NEW.projected_alpha_bn := :NEW.q1_2026_revenue_bn * c_factor;
    ELSE
        :NEW.projected_alpha_bn := :NEW.q1_2026_revenue_bn * 1.05;
    END IF;
END;
/

