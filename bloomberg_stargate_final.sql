-- Formatação Bloomberg Terminal Style
SELECT 
    PERIOD AS [STRATEGIC PERIOD],
    BANK_ENTITY AS [MARKET INSTITUTION],
    FORMAT(PROJECTED_REVENUE_USD, 'C', 'en-US') AS [REVENUE],
    FORMAT(CUMULATIVE_AUM_USD, 'C', 'en-US') AS [CUMULATIVE AUM],
    CONFIDENCE_MATRIX AS [RISK],
    MISSION_STATUS AS [STATUS]
FROM BLOOMBERG_MASTER_REPORT
ORDER BY CUMULATIVE_AUM_USD ASC;
GO
