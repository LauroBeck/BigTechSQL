USE STARGATE_ANALYTICS;
GO

-- Limpeza para reconstrução de alta fidelidade
IF OBJECT_ID('BLOOMBERG_MASTER_REPORT', 'U') IS NOT NULL DROP TABLE BLOOMBERG_MASTER_REPORT;
GO

CREATE TABLE BLOOMBERG_MASTER_REPORT (
    PERIOD VARCHAR(12),
    BANK_ENTITY VARCHAR(30),
    PROJECTED_REVENUE_USD MONEY,
    CUMULATIVE_AUM_USD MONEY,
    CONFIDENCE_MATRIX VARCHAR(15),
    MISSION_STATUS VARCHAR(20)
);
GO

