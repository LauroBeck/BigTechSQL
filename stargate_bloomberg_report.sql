WITH TimeSeries(MonthIdx, ProjectionDate) AS (
    SELECT 1, ADD_MONTHS(TRUNC(SYSDATE, 'MM'), 1) FROM DUAL
    UNION ALL
    SELECT MonthIdx + 1, ADD_MONTHS(ProjectionDate, 1)
    FROM TimeSeries WHERE MonthIdx < 36
),
BankList AS (
    SELECT 'JPMORGAN CHASE & CO' as entity_name, 6 as m_idx FROM DUAL UNION ALL
    SELECT 'GOLDMAN SACHS GROUP' as entity_name, 12 as m_idx FROM DUAL UNION ALL
    SELECT 'BANK OF AMERICA CORP' as entity_name, 18 as m_idx FROM DUAL UNION ALL
    SELECT 'MORGAN STANLEY' as entity_name, 24 as m_idx FROM DUAL UNION ALL
    SELECT 'BNY MELLON CORP' as entity_name, 30 as m_idx FROM DUAL UNION ALL
    SELECT 'CITIGROUP INC' as entity_name, 36 as m_idx FROM DUAL
),
GrowthFactors AS (
    SELECT (1.26 * 0.02) as MonthlyGrowthRate FROM DUAL
)
SELECT 
    TO_CHAR(t.ProjectionDate, 'MON-YYYY') as "PERIOD",
    bl.entity_name as "ENTITY",
    (2663806.33 * POWER(1 + g.MonthlyGrowthRate, t.MonthIdx)) as "PROJ_REV",
    (9800000 * 126 * POWER(1.01, t.MonthIdx)) as "CUM_AUM",
    CASE 
        WHEN t.MonthIdx <= 12 THEN ' [ HIGH ]'
        WHEN t.MonthIdx <= 24 THEN ' [ STABLE ]'
        ELSE ' [ SPEC ]'
    END as "RISK_LVL",
    'MISSION_SUCCESS' as "STATUS"
FROM TimeSeries t
CROSS JOIN GrowthFactors g
JOIN BankList bl ON bl.m_idx = t.MonthIdx
ORDER BY t.MonthIdx;

