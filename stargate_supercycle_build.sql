    TICKER VARCHAR(10) NOT NULL PRIMARY KEY,
    TARGET_18MO DECIMAL(18, 2),
    TARGET_36MO DECIMAL(18, 2),
    BRENT_SUPPORT_FLOOR DECIMAL(18, 2),
    SIGNAL_STRENGTH VARCHAR(20)
);

-- 2. Inject Big Oil & Index Targets
MERGE INTO ENERGY_PROJECTIONS AS target
USING (VALUES 
    ('CVX', 215.00, 251.00, 95.00, 'HIGH'), 
    ('BP', 52.50, 64.00, 95.00, 'MEDIUM_HIGH'), 
    ('ES_FUT', 7450.00, 8100.00, NULL, 'BULLISH')
) AS source (TICKER, T18, T36, BRENT, SIGNAL)
ON target.TICKER = source.TICKER
WHEN MATCHED THEN 
    UPDATE SET TARGET_18MO = source.T18, TARGET_36MO = source.T36, BRENT_SUPPORT_FLOOR = source.BRENT, SIGNAL_STRENGTH = source.SIGNAL
WHEN NOT MATCHED THEN 
    INSERT (TICKER, TARGET_18MO, TARGET_36MO, BRENT_SUPPORT_FLOOR, SIGNAL_STRENGTH)
    VALUES (source.TICKER, source.T18, source.T36, source.BRENT, source.SIGNAL);

-- 3. Consolidated Supercycle & Market Telemetry Report
WITH BIG_TECH_SUPERCYCLE (SEGMENT, Q1_2026, M18_PROJ, M36_PROJ, ALPHA_DRIVER) AS (
    VALUES 
    ('IBM Infrastructure', 3.30, 4.20, 5.10, 'Z17 / Telum II Adoption'),
    ('IBM Software', 7.10, 9.15, 11.80, 'Watsonx Data Expansion'),
    ('Mag7 Core ($T)', 14.30, 17.80, 21.40, 'AI ROI & Data Center Expansion')
),
SUPERCYCLE_TOTALS AS (
    SELECT 
        'TOTAL IBM ALPHA' as SEGMENT, 
        SUM(CASE WHEN SEGMENT LIKE 'IBM%' THEN Q1_2026 ELSE 0 END) as Q1_2026, 
        SUM(CASE WHEN SEGMENT LIKE 'IBM%' THEN M18_PROJ ELSE 0 END) as M18_PROJ, 
        SUM(CASE WHEN SEGMENT LIKE 'IBM%' THEN M36_PROJ ELSE 0 END) as M36_PROJ, 
        'Consolidated Big Iron Scaling' as ALPHA_DRIVER
    FROM BIG_TECH_SUPERCYCLE
),
TOMORROW_PROJECTION (INDEX_NAME, CURRENT_VAL, REBOUND_TARGET) AS (
    VALUES 
    ('S&P 500 FUT', 7179.25, 7500.00),
    ('NASDAQ-100', 24663.80, 25800.00)
),
CONSOLIDATED_RESULTS AS (
    SELECT 
        CAST(SEGMENT AS VARCHAR(25)) AS LABEL,
        Q1_2026 AS VAL_A,
        M18_PROJ AS VAL_B,
        M36_PROJ AS VAL_C,
        CAST(ALPHA_DRIVER AS VARCHAR(40)) AS METADATA,
        'FINANCIAL_PROJECTION' AS CAT
    FROM BIG_TECH_SUPERCYCLE
    UNION ALL
    SELECT 
        CAST(SEGMENT AS VARCHAR(25)), Q1_2026, M18_PROJ, M36_PROJ, 
        CAST(ALPHA_DRIVER AS VARCHAR(40)), 'SUMMARY'
    FROM SUPERCYCLE_TOTALS
    UNION ALL
    SELECT 
        CAST(INDEX_NAME AS VARCHAR(25)), CURRENT_VAL, REBOUND_TARGET, 
        CAST(((REBOUND_TARGET - CURRENT_VAL) / CURRENT_VAL * 100) AS DECIMAL(5,2)),
        CASE WHEN CURRENT_VAL > 7150 THEN 'PIVOT_HOLD_ACTIVE' ELSE 'RETEST' END,
        'MARKET_TELEMETRY'
    FROM TOMORROW_PROJECTION
)
SELECT 
    LABEL AS "SEGMENT/INDEX",
    CAST(VAL_A AS DECIMAL(14,2)) AS "Q1_2026 / CURRENT",
    CAST(VAL_B AS DECIMAL(14,2)) AS "18-MO / TARGET",
    CAST(VAL_C AS DECIMAL(14,2)) AS "36-MO / GAP_%",
    METADATA AS "PRIMARY_DRIVER / STATE"
FROM CONSOLIDATED_RESULTS
ORDER BY CAT DESC;

TERMINATE;

