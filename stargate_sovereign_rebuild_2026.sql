-- 3. THE STARGATE TRIGGER: 1.42x SOVEREIGN ALPHA
CREATE OR REPLACE TRIGGER STARGATE_ADMIN.TRG_SOVEREIGN_CALC
BEFORE INSERT OR UPDATE ON STARGATE_ADMIN.STARGATE_TELEMETRY
FOR EACH ROW
DECLARE
    c_stargate_constant CONSTANT NUMBER := 1.42;
BEGIN
    IF :NEW.is_quantum_ready = TRUE THEN
        :NEW.alpha_proj_bn := :NEW.q1_26_rev_bn * c_stargate_constant;
    ELSE
        :NEW.alpha_proj_bn := :NEW.q1_26_rev_bn * 1.05;
    END IF;
END;
